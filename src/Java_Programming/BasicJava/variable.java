public class variable {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
//        literals : which do not change like numbers , universal characters
        int a , b , sum;
//         the name of our variables are called identifiers like a , b etc.
        a = 10;
        b = 20;
        sum = a + b;
//        System.out.println("sum of a and b is :" sum);
        System.out.println(sum);
        a = 4;
        b = 5;
//        value will get override of a and b
        System.out.println(a);
        System.out.println(b);


    }
}
