public class Hello {
    public static void main(String[] args) {
//        print function will print all statement continuously
        System.out.print("Hello");
        System.out.print("Hello");
        System.out.print("Hello");
//        println function will give output in the next line
        System.out.println("Hello");
        System.out.println("Hello");
        System.out.println("Hello");
//        or we can use "\n" for next line in the " "
        System.out.print("Hello\n");
        System.out.print("Hello\n");
        System.out.print("Hello\n");

    }
}
