public class patterns {
    public static void main(String[] args) {
        System.out.println("Pint the 4*4 '*' pattern ");
        int i = 0;
        int j = 0;
        int n = 4;
        for( i = 0; i < n; i++){
            for(j = 0; j < n; j++){
                System.out.print("*");
            }
            System.out.println();

        }
    }
}
